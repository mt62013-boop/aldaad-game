function showQuestions(domain, branch) {
  let qs = [];
  if (branch) {
    qs = questions[branch] || [];
  } else {
    qs = questions[domain] || [];
  }
  const generateBtn = document.getElementById('generateQuestionBtn');
  if (!qs.length) {
    questionArea.textContent = 'لا توجد أسئلة حالياً.';
    answerForm.innerHTML = '';
    resultArea.textContent = '';
    questionSection.style.display = '';
    branchSection.style.display = 'none';
    if (generateBtn) generateBtn.style.display = 'none';
    return;
  }
  let idx = 0;
  function renderQ() {
    const q = qs[idx];
    questionArea.textContent = q.q;
    answerForm.innerHTML = '';
    resultArea.textContent = '';
    if (q.type === 'tf') {
      ['صح', 'خطأ'].forEach(opt => {
        const btn = document.createElement('button');
        btn.type = 'button';
        btn.textContent = opt;
        btn.onclick = () => checkAnswer(opt);
        answerForm.appendChild(btn);
      });
    } else if (q.type === 'mcq') {
      q.options.forEach(opt => {
        const btn = document.createElement('button');
        btn.type = 'button';
        btn.textContent = opt;
        btn.onclick = () => checkAnswer(opt);
        answerForm.appendChild(btn);
      });
    }
    if (generateBtn) generateBtn.style.display = '';
  }
  function checkAnswer(ans) {
    if (ans === qs[idx].answer) {
      resultArea.textContent = 'إجابة صحيحة ✔';
    } else {
      resultArea.textContent = 'إجابة خاطئة ✖';
    }
    idx++;
    if (idx < qs.length) {
      setTimeout(renderQ, 1200);
    } else {
      setTimeout(() => {
        questionSection.style.display = 'none';
        if (branch) branchSection.style.display = '';
        else domainSection.style.display = '';
      }, 1500);
    }
  }
  if (generateBtn) {
    generateBtn.onclick = function() {
      // توليد سؤال عشوائي أو ذكي (نموذج أولي)
      const allQs = questions[branch] || questions[domain] || [];
      if (allQs.length) {
        idx = Math.floor(Math.random() * allQs.length);
        renderQ();
      }
    };
  }
  questionSection.style.display = '';
  domainSection.style.display = 'none';
  branchSection.style.display = 'none';
  renderQ();
}
